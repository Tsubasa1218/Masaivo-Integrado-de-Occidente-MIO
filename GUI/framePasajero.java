    private void buscarCodigoConsultarSolicitudMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarCodigoConsultarSolicitudMouseClicked
        // TODO add your handling code here:
        String tiquete = codigoConsultarSolicitud.getText();
        Solicitud solicitud = new DAOSolicitud().obtenerSolicitud(tiquete);
        
        if(solicitud.isEstadSolicitud()){
            estadoSolicitud.setText("Activa");
        }else{
            estadoSolicitud.setText("Inactiva");
        }
        motivoSolicitud.setText(solicitud.getMotivoSolicitud());
        descripcionSolicitud.setText(solicitud.getDescripcionSolicitud());
        respuestaSolicitud.setText(solicitud.getRespuesta());
        nombreSolicitud.setText(new DAOPasajero().obtenerNombrePasajero(tiquete));
        estacionSolicitud.setText(new DAOEstacion().consultarEstacionSolicitud(tiquete));
    }//GEN-LAST:event_buscarCodigoConsultarSolicitudMouseClicked

    private void buscarCodigoConsultarSaldoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarCodigoConsultarSaldoMouseClicked
        String codigo_tarjeta = codigoConsultar.getText();
        int numeroPasajes = new DAOTarjeta().consultarTarjeta(codigo_tarjeta).getNumero_pasajes();
        pasajesActualesConsultar.setText(Integer.toString(numeroPasajes));
        
    }//GEN-LAST:event_buscarCodigoConsultarSaldoMouseClicked

