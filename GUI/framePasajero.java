    private void abordarRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abordarRutaActionPerformed
        // TODO add your handling code here:
        int pin = Integer.parseInt(pinTarjetaAbordar.getText());
        Tarjeta tarjeta = new DAOTarjeta().consultarTarjeta(pinTarjetaAbordar.getText());
        String estacion_nombre = rutaAbordar.getSelectedItem().toString();
        int ruta = new DAORuta().consultarRuta(estacion_nombre);
        int add = new DAOTarjeta().abordarBus(pin, ruta);
        if(add > 0){
            JOptionPane.showMessageDialog(null, "bus abordado");
            new DAOTarjeta().modificarTarjeta(tarjeta);
        }else{
            JOptionPane.showMessageDialog(null, "Error, pase la tarjeta otra vez");
        }
    }//GEN-LAST:event_abordarRutaActionPerformed

